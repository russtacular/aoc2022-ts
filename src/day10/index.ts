import run from "aocrunner";
import { sum } from "../utils/index.js";

const parseInput = (rawInput: string) =>
  rawInput.split('\n').map(r =>
    r.startsWith('addx') ? [0, parseInt(r.split(' ')[1], 10)] : 0
  ).flat();

const getXReg = (cycle: number, instructions: number[]) =>
  cycle * instructions.slice(0, cycle-1).reduce(sum, 1);

const part1 = (rawInput: string) => {
  const input = parseInput(rawInput);
  const keyCycles = [20, 60, 100, 140, 180, 220];
  return keyCycles.map(c => getXReg(c, input)).reduce(sum, 0);
};

const paint = (cycle: number, xReg: number) => {
  const addSpriteWidth = 2;
  const pixels = ['.', '#'];
  return pixels[+(xReg <= cycle && cycle <= (xReg + addSpriteWidth))];
};

const part2 = (rawInput: string) => {
  const input = parseInput(rawInput);
  const scrW = 40;
  const scrH = 6;
  const screen: string[][] = new Array(scrH).fill(new Array(scrW).fill(' '));

  const res = screen.map((row, yy) => {
    const cycleOffset = yy * scrW; 
    return row.map((_, idx) => {
      const xReg = input.slice(0, Math.min(cycleOffset + idx, input.length)).reduce(sum, 1);
      return paint(idx, xReg-1); // screen is 0-indexed, X register is 1-indexed
    }).join('');  
  }).join('\n');

  return res; // prints to screen automatically with aocrunner
};

